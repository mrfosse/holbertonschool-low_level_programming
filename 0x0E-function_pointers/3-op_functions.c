/**
 * op_mul - multiples int a to int b.
 * @a: int one
 * @b: int two
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides int a into int b.
 * @a: int one
 * @b: int two
 * Return: a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - divides int a into int b and returns the remainder.
 * @a: int one
 * @b: int two
 * Return: a%b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
