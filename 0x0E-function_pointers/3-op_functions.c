#include "3-calc.h"
/**
 * op_add - adds int a to int b.
 * @a: int one
 * @b: int two
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracks int a from int b.
 * @a: int one
 * @b: int two
 * Return: difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
